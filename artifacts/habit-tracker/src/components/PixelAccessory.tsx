function fillFor(ch: string): string {
  if (ch === ".") return "transparent";
  return PALETTE[ch] ?? "#9333ea";
}

export function PixelAccessory({ id, size = 48 }: { id: string; size?: number }) {
  const blueprint = BLUEPRINTS[id];
  const cols = 16;
  const rows = 14;
  if (!blueprint) {
    return (
      <span
        className="inline-flex items-center justify-center font-black tabular-nums text-muted-foreground border-2 border-foreground rounded"
        style={{ width: size, height: (size * rows) / cols, fontSize: size * 0.35 }}
      >
        ?
      </span>
    );
  }
  const rects: React.ReactElement[] = [];
  for (let y = 0; y < rows; y++) {
    const row = blueprint[y] ?? ".".repeat(cols);
    for (let x = 0; x < cols; x++) {
      const ch = row[x] ?? ".";
      if (ch === ".") continue;
      const fill = fillFor(ch);
      if (fill === "transparent") continue;
      rects.push(<rect key={`${x}-${y}`} x={x} y={y} width={1} height={1} fill={fill} />);
    }
  }
  return (
    <svg
      viewBox={`0 0 ${cols} ${rows}`}
      width={size}
      height={(size * rows) / cols}
      shapeRendering="crispEdges"
      style={{ imageRendering: "pixelated" }}
      aria-hidden
    >
      {rects}
    </svg>
  );
}
